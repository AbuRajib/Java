package oop.abstraction;

public class DesktopComputer extends Computer implements ComputingDevice{

	
	public void Hardware() {
		System.out.println("Hardware is a physical elements that comprise a computer system");	
	}

	
	public void Ram() {
		System.out.println("RAM stands for Random Access Memory");
	}

	
	public void Rom() {
		System.out.println("ROM stands for Read only Memory");
	}

	
	public void Cpu() {
		System.out.println("CPU stands for Central Processing Unit");
	}

	
	public void notepad() {
		System.out.println("iPad is an iOS-based line of tablet computers designed,marketed by Apple Inc.");
	}

}
